    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        jPanel3.setVisible(true);
        Usuario u = new Usuario(txtAlias.getText(), txtNombres.getText(), txtApellidos.getText(), txtemail.getText(), txtCelular.getText(), txtPassword.getText());
        IUsuarioImpl iu =new IUsuarioImpl();
        iu.agregarUsuario(u);
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        IUsuarioImpl iu =new IUsuarioImpl();
        String respuesta = iu.eliminarUsuario(txtAlias.getText());
        txtMensaje.setText(respuesta);
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        jPanel3.setVisible(true);
        Usuario u = new Usuario(txtAlias.getText(), txtNombres.getText(), txtApellidos.getText(), txtemail.getText(), txtCelular.getText(), txtPassword.getText());
        IUsuarioImpl iu =new IUsuarioImpl();
        iu.actualizarUsuario(txtAlias.getText(), u);
    }//GEN-LAST:event_btnModificarActionPerformed

