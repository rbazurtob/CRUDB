    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        jPanel3.setVisible(true);
        btnEliminar.setEnabled(true);
        btnModificar.setEnabled(true);
        IUsuarioImpl iu = new IUsuarioImpl();
        Usuario u = iu.listarUsuario(txtAlias.getText());
        txtNombres.setText(u.getNombres());
        txtApellidos.setText(u.getApellidos());
        txtemail.setText(u.getEmail());
        txtCelular.setText(u.getCelular());
        txtPassword.setText(u.getCelular());
    }//GEN-LAST:event_btnBuscarActionPerformed

